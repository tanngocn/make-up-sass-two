let row = document.querySelector(".main  .col-sm-12.col-md-12.col-lg-9 .row");

function convertArrayToString(array) {
  let type = "";
  for (let i = 0; i < array.length; i++) {
    if (i == array.length - 1) {
      type += array[i];
    } else {
      type += array[i] + " | ";
    }
  }
  return type;
}
function createBlockStoryFilter(array) {
  array.forEach((element) => {
    // processs array type;
    let wrapperBootStrap = document.createElement("div");
    wrapperBootStrap.classList.add("col-sm-12");
    wrapperBootStrap.classList.add("col-md-6");
    wrapperBootStrap.classList.add("col-lg-6");
    wrapperBootStrap.classList.add(".wrapper-item");
    let item = document.createElement("div");
    item.classList.add("item-story");
    var domString = `<a href="" title=""><img src="${element.img_story}" alt=""/> </a>`;
    domString += ' <div class="content">';
    domString += `<h4> <a href="" title="">  ${element.name_story} </a></h4>`;
    domString += `<p class="type">${convertArrayToString(element.type_Story)}</p> `;
    domString += `<p> ${element.description_story}</p>`;
    domString += `<small><span class="glyphicon glyphicon-copyright-mark"></span>
    ${element.copyright_story}
      <span class="glyphicon glyphicon-star"></span>
    ${element.rating_story}
      <span class="glyphicon glyphicon-eye-open"></span>
    ${element.view_story}
      <span class="glyphicon glyphicon-send"></span>
    ${element.share_story}
      <span class="glyphicon glyphicon-thumbs-up"></span>  
    ${element.like}</small>'`;
      

    domString += "</div>";
    item.innerHTML = domString;
    wrapperBootStrap.appendChild(item);
    row.appendChild(wrapperBootStrap);
  });
}

